export function formatProgress(surahIndex?: number | null, ayah?: number | null, page?: number | null) {
  if (surahIndex == null || ayah == null) return 'غير محدد';
  const name = surahNames[surahIndex - 1] || `سورة #${surahIndex}`;
  let progress = `${name} - آية ${ayah}`;
  if (page) {
    progress += ` - صفحة ${page}`;
  }
  return progress;
}
